/**
 * Enum representing the possible marks in a tic-tac-toe game.
 *
 *  This enum has three values:
 *  - BLANK: Represents an empty space on the board.
 *  - X: Represents the 'X' mark placed by one of the players.
 *  - O: Represents the 'O' mark placed by the other player.
 *
 * The 'BLANK' value is useful for initializing the game board and checking for unmarked spaces
 * during the game.
 */
public enum Mark {
    BLANK,
    X,
    O
}